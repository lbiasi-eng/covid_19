from datetime import datetime

def get_xdata_test():
    FMT = '%Y-%m-%d %H:%M:%S'
    date1 = '2020-03-28 18:00:00'
    date2 = '2020-06-30 18:00:00'
    dates = pd.date_range(date1, date2).tolist()
    return pd.DataFrame(
        list(map(lambda x: (x - datetime.strptime("2020-01-01 00:00:00", FMT)).days, dates)))


